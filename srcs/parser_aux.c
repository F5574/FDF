#include "../inc/fdf.h"

int	is_signals(int c)
{
	if (c == '+' || c == '-')
		return (1);
	return (0);
}

int	is_color(int c)
{
	if (c == ',' || c == 'x')
		return (1);
	if ((c >= 'a' && c <= 'f') || (c >= 'A' && c <= 'F'))
		return (1);
	return (0);
}
